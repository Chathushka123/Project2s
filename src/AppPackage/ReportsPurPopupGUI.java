    private void loadTbl(){
        try{
            DefaultTableModel dtm = (DefaultTableModel)jTable_reportspopup.getModel();
            ResultSet rs = mc.getData("SELECT id, name, description, purchase_unit, purchase_price FROM medicine");
            while(rs.next()){
                Vector vtr = new Vector();
                vtr.add(rs.getString("id"));
                vtr.add(rs.getString("name"));
                vtr.add(rs.getString("description"));
                vtr.add(rs.getString("purchase_unit"));
                vtr.add(rs.getString("purchase_price"));
                dtm.addRow(vtr);
            }
        }
        catch(Exception exc){
            exc.printStackTrace();
        }
    }
