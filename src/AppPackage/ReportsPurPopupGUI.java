    private void jTable_reportspopupMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_reportspopupMouseClicked
        DefaultTableModel dtm = (DefaultTableModel)jTable_reportspopup.getModel();
        int sri = jTable_reportspopup.getSelectedRow();
        sri_id = dtm.getValueAt(sri, 0).toString();
        ReportsGUI reports = new ReportsGUI();
        reports.jTextField_reportspurchasemedicineid.setText(sri_id);
        reports.setPurMediName();
        reports.setVisible(true);
        reports.pack();
        setVisible(false);
        dispose();
    }//GEN-LAST:event_jTable_reportspopupMouseClicked

    private void jTextField_reportpopupfindKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField_reportpopupfindKeyReleased
        DefaultTableModel dtm = (DefaultTableModel)jTable_reportspopup.getModel();
        TableRowSorter<DefaultTableModel> trs = new TableRowSorter<DefaultTableModel>(dtm);
        jTable_reportspopup.setRowSorter(trs);
        String search = jTextField_reportpopupfind.getText();
        trs.setRowFilter(RowFilter.regexFilter(search));
    }//GEN-LAST:event_jTextField_reportpopupfindKeyReleased

