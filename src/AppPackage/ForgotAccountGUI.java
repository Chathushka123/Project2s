    private void jButton_forgotaccountsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_forgotaccountsearchActionPerformed
        if(jTextField_forgotaccountemail.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Please enter the email id!");
        }
        else{
            try{
                ResultSet rs = mc.getData("SELECT username FROM user WHERE email = '"+jTextField_forgotaccountemail.getText()+"'");
                if(rs.first()){
                    visibleArea();
                    jTextField_forgotaccountusername.setText(rs.getString("username"));
                }
                else{
                    JOptionPane.showMessageDialog(null, "No email id found!");
                }
            }
            catch(Exception exc){
                    JOptionPane.showMessageDialog(null, exc);
            }
        }
    }//GEN-LAST:event_jButton_forgotaccountsearchActionPerformed

