    private void jButton_forgotaccountresetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_forgotaccountresetActionPerformed
        if(jPasswordField_forgotaccountpassword.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Please enter new password!");
        }
        else{
            try{
                mc.putData("UPDATE user SET password = '"+jPasswordField_forgotaccountpassword.getText()+"' WHERE email = '"+jTextField_forgotaccountemail.getText()+"'");
                JOptionPane.showMessageDialog(null, "Password updated!");
                LoginGUI login = new LoginGUI();
                login.setVisible(true);
                setVisible(false);
                dispose();
            }
            catch(Exception exc){
                exc.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton_forgotaccountresetActionPerformed

