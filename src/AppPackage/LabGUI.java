    private void loadTbl(){
        try{
            DefaultTableModel dtm = (DefaultTableModel)jTable_labview.getModel();
            ResultSet rs = mc.getData("SELECT lab.id, patient.name, lab.amount, lab.discount, lab.net_amount, lab.date FROM lab, patient WHERE lab.patient = patient.id");
            while(rs.next()){
                Vector vtr = new Vector();
                vtr.add(rs.getString("lab.id"));
                vtr.add(rs.getString("lab.date"));
                vtr.add(rs.getString("patient.name"));
                vtr.add(rs.getString("lab.amount"));
                vtr.add(rs.getString("lab.discount"));
                vtr.add(rs.getString("lab.net_amount"));
                dtm.addRow(vtr);
            }
        }
        catch(Exception exc){
            exc.printStackTrace();
        }
    }
