    private void loadSumTbl(){
        try{
            String starttime = ((JTextField)jDateChooser_reportssummarystartdate.getDateEditor().getUiComponent()).getText();
            String start = starttime + " 00:00:00.0";
            String endtime = ((JTextField)jDateChooser_reportssummaryenddate.getDateEditor().getUiComponent()).getText();
            String end = endtime + " 11:59:59.9";
            DefaultTableModel dtm = (DefaultTableModel)jTable_reportssummary.getModel();
            
            Statement stmt = MysqlConnection.conn().createStatement();
            
            ResultSet rspur = stmt.executeQuery("SELECT medicine.name, medicine.purchase_price, medicine.sales_unit, medicine.unit_relation, purchase_item.quantity, purchase_item.amount FROM medicine, purchase_item, purchase WHERE medicine.id = '"+jTextField_reportssummarymedicineid.getText()+"' AND purchase_item.medicine = '"+jTextField_reportssummarymedicineid.getText()+"' AND purchase_item.purchase = purchase.id AND DATE(purchase.date) BETWEEN '"+start+"' AND '"+end+"'");
            tblpurqty = 0.0;
            tblpuramt = new BigDecimal("0.00");
            while(rspur.next()){
                name = rspur.getString("medicine.name");
                unit = rspur.getString("medicine.sales_unit");
                unitrelation = rspur.getDouble("medicine.unit_relation");
                double qty = rspur.getDouble("purchase_item.quantity") * unitrelation;
                tblpurqty = tblpurqty + qty;
                tblpuramt = tblpuramt.add(new BigDecimal(rspur.getString("purchase_item.amount")));
            }
            
            ResultSet rssal = stmt.executeQuery("SELECT sales_item.quantity, sales_item.amount FROM sales_item, sales WHERE sales_item.medicine = '"+jTextField_reportssummarymedicineid.getText()+"' AND sales_item.sales = sales.id AND DATE(sales.date) BETWEEN '"+start+"' AND '"+end+"'");
            tblsalqty = 0.0;
            tblsalamt = new BigDecimal("0.00");
            while(rssal.next()){
                tblsalqty = tblsalqty + rssal.getDouble("sales_item.quantity");
                tblsalamt = tblsalamt.add(new BigDecimal(rssal.getString("sales_item.amount")));
            }
            
            tblstkqty = 0.0;
            tblstkqty = tblpurqty - tblsalqty;
            
            purprz = new BigDecimal("0.00");
            purprz = tblpuramt.divide(new BigDecimal(tblpurqty), 5, BigDecimal.ROUND_FLOOR);
            tblstkamt = new BigDecimal("0.00");
            tblstkamt = purprz.multiply(new BigDecimal(tblstkqty));
            
            tblprofit = new BigDecimal("0.00");
            tblprofit = (tblsalamt.add(tblstkamt).subtract(tblpuramt));
            
            int i = 0;
            while(i < 1){
                Vector vtr = new Vector();
                vtr.add(name);
                vtr.add(unit);
                vtr.add(String.format("%.2f", tblpurqty));
                vtr.add(String.format("%.2f", tblpuramt));
                vtr.add(String.format("%.2f", tblsalqty));
                vtr.add(String.format("%.2f", tblsalamt));
                vtr.add(String.format("%.2f", tblstkqty));
                vtr.add(String.format("%.2f", tblstkamt));
                vtr.add(String.format("%.2f", tblprofit));
                dtm.addRow(vtr);
                i++;
            }
        }
        catch(Exception exc){
            exc.printStackTrace();
        }
    }
    
