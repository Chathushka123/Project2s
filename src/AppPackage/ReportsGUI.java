    public void setPurMediName(){
        String search;
        search = jTextField_reportspurchasemedicineid.getText();
        try{
            ResultSet rs = mc.getData("SELECT name FROM medicine WHERE id = '"+search+"'");
            if(rs.first()){
                jTextField_reportspurchasemedicinename.setText(rs.getString("name"));
            }
            else{
                jTextField_reportspurchasemedicinename.setText(null);
            }
	}
	catch(Exception exc){
		JOptionPane.showMessageDialog(null, exc);
	}                
    }
    
