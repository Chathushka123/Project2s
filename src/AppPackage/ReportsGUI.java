    private void jButton_reportspurchasegenerateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_reportspurchasegenerateActionPerformed
        if(jTextField_reportspurchasemedicineid.getText().isEmpty() || ((JTextField)jDateChooser_reportspurchasestartdate.getDateEditor().getUiComponent()).getText().isEmpty() || ((JTextField)jDateChooser_reportspurchaseenddate.getDateEditor().getUiComponent()).getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Please fill all fields!");
        }
        else{
            loadPurTbl();
            double totalqty;
            BigDecimal totalnet;
            jTextField_reportspurchasemedicineid.setText(null);
            ((JTextField)jDateChooser_reportspurchasestartdate.getDateEditor().getUiComponent()).setText(null);
            ((JTextField)jDateChooser_reportspurchaseenddate.getDateEditor().getUiComponent()).setText(null);
            int trc = jTable_reportspurchase.getRowCount();
            if(trc > 0){
                totalnet = new BigDecimal("0");
                for(int i = 0; i < trc; i++){
                    totalnet = totalnet.add(new BigDecimal(jTable_reportspurchase.getValueAt(i, 6).toString()));
                }
                jTextField_reportspurchasetotalnet.setText(String.format("%.2f", totalnet));
            }
            else{
                jTextField_reportspurchasetotalnet.setText("0.00");
            }
            if(trc > 0){
                totalqty = 0.00;
                for(int i = 0; i < trc; i++){
                    totalqty += Double.parseDouble(jTable_reportspurchase.getValueAt(i, 5).toString());
                }
                jTextField_reportspurchasetotalqty.setText(String.format("%.2f", totalqty));
            }
            else{
                jTextField_reportspurchasetotalqty.setText("0.00");
            }
        }
    }//GEN-LAST:event_jButton_reportspurchasegenerateActionPerformed

