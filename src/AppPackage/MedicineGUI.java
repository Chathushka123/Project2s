    public void excelExport(){
        XSSFWorkbook workbook = new XSSFWorkbook();
        XSSFSheet sheet = workbook.createSheet("Medicines Detail");
        try{
            ResultSet rs = mc.getData("SELECT id, name, description, purchase_unit, purchase_price, sales_unit, sales_price, exp_date, unit_relation, quantity FROM medicine");
            Map<String, Object[]> data = new TreeMap<String, Object[]>();
            data.put("1", new Object[]{"ID", "NAME", "DESCRIPTION", "PURCHASED UNIT", "PURCHASE PRICE", "SALES UNIT", "SALES PRICE", "EXPIRE DATE", "UNIT RELATION", "QUANTITY"});
            String srow = "2";
            while(rs.next()){
                data.put(srow, new Object[]{rs.getString("id"), rs.getString("name"), rs.getString("description"), rs.getString("purchase_unit"), rs.getString("purchase_price"), rs.getString("sales_unit"), rs.getString("sales_price"), rs.getString("exp_date"), rs.getString("unit_relation"), rs.getString("quantity")});
                srow += "1";
            }
            Set<String> keyset = data.keySet();
            int rownum = 0;
            for(String key : keyset){
                Row row = sheet.createRow(rownum++);
                Object[] objArr = data.get(key);
                int cellnum = 0;
                for(Object obj : objArr){
                    Cell cell = row.createCell(cellnum++);
                    if(obj instanceof String){
                        cell.setCellValue((String)obj);
                    }
                    else if(obj instanceof Integer){
                        cell.setCellValue((Integer)obj);
                    }
                }
            }
            FileOutputStream out = new FileOutputStream(new File("C:\\Users\\Chathushka\\Desktop\\medicines_detail.xlsx"));
            workbook.write(out);
            out.close();
            JOptionPane.showMessageDialog(null, "The file \"medicines_detail.xlsx\" exported to desktop successfully!");
        }
        catch(Exception exc){
            exc.printStackTrace();
        }
    }    
    
    public void excelImport(String filename){
        this.filename = filename;
        try{
            FileInputStream file = new FileInputStream(new File(this.filename));
            XSSFWorkbook workbook = new XSSFWorkbook(file);
            XSSFSheet sheet = workbook.getSheetAt(0);
            
            Iterator<Row> rowIterator = sheet.rowIterator();
            while(rowIterator.hasNext()){
                Row row = rowIterator.next();
                genId();
                Iterator<Cell> cellIterator = row.cellIterator();
                int i = 0;
                while(cellIterator.hasNext()){
                    Cell cell = cellIterator.next();
                    if(cell.getCellTypeEnum() == CellType.STRING){
                        data[i] = cell.getStringCellValue();
                        i++;
                    }
                }
                mc.putData("INSERT INTO medicine (id, name, description, purchase_unit, purchase_price, sales_unit, sales_price, exp_date, unit_relation, quantity) VALUES ('"+sid+"', '"+data[0]+"', '"+data[1]+"', '"+data[2]+"', '"+data[3]+"', '"+data[4]+"', '"+data[5]+"', '"+data[6]+"', '"+data[7]+"', '"+data[8]+"')");
            }
            file.close();
            JOptionPane.showMessageDialog(null, "The data imported successfully!");
        }
        catch(Exception exc){
            exc.printStackTrace();
        }
    }

