    public void loadTbl(){
        try{
            DefaultTableModel dtm = (DefaultTableModel)jTable_medicineview.getModel();
            ResultSet rs = mc.getData("SELECT id, name, description, purchase_unit, purchase_price, sales_unit, sales_price, exp_date, unit_relation, quantity FROM medicine");
            while(rs.next()){
                Vector vtr = new Vector();
                vtr.add(rs.getString("id"));
                vtr.add(rs.getString("name"));
                vtr.add(rs.getString("description"));
                vtr.add(rs.getString("purchase_unit"));
                vtr.add(rs.getString("purchase_price"));
                vtr.add(rs.getString("sales_unit"));
                vtr.add(rs.getString("sales_price"));
                vtr.add(rs.getString("exp_date"));
                vtr.add(rs.getString("unit_relation"));
                vtr.add(rs.getString("quantity"));
                dtm.addRow(vtr);
            }
        }
        catch(Exception exc){
            exc.printStackTrace();
        }
    }
