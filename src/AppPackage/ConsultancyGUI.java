    private void loadTbl(){
        try{
            DefaultTableModel dtm = (DefaultTableModel)jTable_consultancyview.getModel();
            ResultSet rs = mc.getData("SELECT consultancy.id, patient.name, doctor.name, consultancy.amount, consultancy.discount, consultancy.net_amount, consultancy.date FROM consultancy, patient, doctor WHERE consultancy.patient = patient.id AND consultancy.doctor = doctor.id");
            while(rs.next()){
                Vector vtr = new Vector();
                vtr.add(rs.getString("consultancy.id"));
                vtr.add(rs.getString("consultancy.date"));
                vtr.add(rs.getString("patient.name"));
                vtr.add(rs.getString("doctor.name"));
                vtr.add(rs.getString("consultancy.amount"));
                vtr.add(rs.getString("consultancy.discount"));
                vtr.add(rs.getString("consultancy.net_amount"));
                dtm.addRow(vtr);
            }
        }
        catch(Exception exc){
            exc.printStackTrace();
        }
    }
