    private void jButton_consultancyviewdeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_consultancyviewdeleteActionPerformed
        int sri[] = jTable_consultancyview.getSelectedRows();
        String sri_id[] = new String[sri.length];
        DefaultTableModel dtm = (DefaultTableModel)jTable_consultancyview.getModel();
        if(sri.length > 0){
            try{
                for(int i = sri.length - 1; i >= 0; i--){
                    sri_id[i] = jTable_consultancyview.getModel().getValueAt(sri[i], 0).toString();
                    Statement stmt = MysqlConnection.conn().createStatement();
                    stmt.executeUpdate("DELETE FROM consultancy WHERE id = '"+sri_id[i]+"'");
                    dtm.removeRow(sri[i]);
                }
                dtm.setRowCount(0);
                loadTbl();
                JOptionPane.showMessageDialog(null, "Data deleted successfully!");
            }
            catch(Exception exc){
                exc.printStackTrace();
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Please select the row to delete data!");
        }
    }//GEN-LAST:event_jButton_consultancyviewdeleteActionPerformed

