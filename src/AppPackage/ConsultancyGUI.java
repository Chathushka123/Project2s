    public void currentDate(){
        Thread currentDate = new Thread(){
            public void run(){
                try{
                    for(;;){
                        Calendar clr = new GregorianCalendar();
                        int year = clr.get(Calendar.YEAR);
                        int month = clr.get(Calendar.MONTH);
                        month++; 
                        int day = clr.get(Calendar.DAY_OF_MONTH);
                        int hour = clr.get(Calendar.HOUR);
                        int minute = clr.get(Calendar.MINUTE);
                        int second = clr.get(Calendar.SECOND);
                        String syear = String.format("%04d", year);
                        String smonth = String.format("%02d", month);
                        String sday = String.format("%02d", day);
                        String shour = String.format("%02d", hour);
                        String sminute = String.format("%02d", minute);
                        String ssecond = String.format("%02d", second);
                        jTextField_consultancycreatedate.setText(syear + "-" + smonth + "-" + sday + " " + shour + ":" + sminute + ":" + ssecond);
                        sleep(1000);
                    }
                }
                catch(Exception exc){
                    exc.printStackTrace();
                }
            }
        };
        currentDate.start();
    }    
    
    public void netAmount(){
        if(!jTextField_consultancycreateamount.getText().isEmpty()){
            amount = new BigDecimal(jTextField_consultancycreateamount.getText());
        }
        else{
            amount = new BigDecimal("0.00");
        }
        if(!jTextField_consultancycreatediscount.getText().isEmpty()){
            discount = new BigDecimal(jTextField_consultancycreatediscount.getText());
        }
        else{
            discount = new BigDecimal("0.00");
        }
        netamount = amount.subtract(discount);
        jTextField_consultancycreatenetamount.setText(String.format("%.2f", netamount));
    }
    
    
    
    
