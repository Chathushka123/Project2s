    private void jButton_accountssummarygenerateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_accountssummarygenerateActionPerformed
        if(((JTextField)jDateChooser_accountssummarystartdate.getDateEditor().getUiComponent()).getText().isEmpty() || ((JTextField)jDateChooser_accountssummaryenddate.getDateEditor().getUiComponent()).getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Please fill all fields!");
        }
        else{
            loadSalSumTbl();
            consrt = summary + 1;
            loadConSumTbl();
            labsrt = summary + 1;
            loadLabSumTbl();
            ((JTextField)jDateChooser_accountssummarystartdate.getDateEditor().getUiComponent()).setText(null);
            ((JTextField)jDateChooser_accountssummaryenddate.getDateEditor().getUiComponent()).setText(null);
            int trc = jTable_accountssummary.getRowCount();
            if(trc > 0){
                totalsal = new BigDecimal("0.00");
                while(section < consrt){
                    totalsal = totalsal.add(new BigDecimal(jTable_accountssummary.getValueAt(section, 5).toString()));
                    section++;
                }
                jTextField_accountssummarytotalsal.setText(String.format("%.2f", totalsal));
                
                totalcon = new BigDecimal("0.00");
                while(section < labsrt){
                    totalcon = totalcon.add(new BigDecimal(jTable_accountssummary.getValueAt(section, 5).toString()));
                    section++;
                }
                jTextField_accountssummarytotalcon.setText(String.format("%.2f", totalcon));
                
                totallab = new BigDecimal("0.00");
                while(section < trc){
                    totallab = totallab.add(new BigDecimal(jTable_accountssummary.getValueAt(section, 5).toString()));
                    section++;
                }
                jTextField_accountssummarytotallab.setText(String.format("%.2f", totallab));
                income = new BigDecimal("0.00");
                income = (totalsal.add(totalcon)).add(totallab);
                jTextField_accountssummaryincome.setText(String.format("%.2f", income));
            }
            else{
                jTextField_accountssummarytotalsal.setText("0.00");
                jTextField_accountssummarytotalcon.setText("0.00");
                jTextField_accountssummarytotallab.setText("0.00");
                jTextField_accountssummaryincome.setText("0.00");
            }
        }
    }//GEN-LAST:event_jButton_accountssummarygenerateActionPerformed

