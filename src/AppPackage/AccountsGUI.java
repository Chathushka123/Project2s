    private void jButton_accountssalesgenerateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_accountssalesgenerateActionPerformed
        if(((JTextField)jDateChooser_accountssalesstartdate.getDateEditor().getUiComponent()).getText().isEmpty() || ((JTextField)jDateChooser_accountssalesenddate.getDateEditor().getUiComponent()).getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Please fill all fields!");
        }
        else{
            loadSalTbl();
            ((JTextField)jDateChooser_accountssalesstartdate.getDateEditor().getUiComponent()).setText(null);
            ((JTextField)jDateChooser_accountssalesenddate.getDateEditor().getUiComponent()).setText(null);
            int trc = jTable_accountssales.getRowCount();
            if(trc > 0){
                totalamt = new BigDecimal("0");
                for(int i = 0; i < trc; i++){
                    totalamt = totalamt.add(new BigDecimal(jTable_accountssales.getValueAt(i, 3).toString()));
                }
                jTextField_accountssalestotalamt.setText(String.format("%.2f", totalamt));
            }
            else{
                jTextField_accountssalestotalamt.setText("0.00");
            }
            if(trc > 0){
                totaldis = new BigDecimal("0");
                for(int i = 0; i < trc; i++){
                    totaldis = totaldis.add(new BigDecimal(jTable_accountssales.getValueAt(i, 4).toString()));
                }
                jTextField_accountssalestotaldis.setText(String.format("%.2f", totaldis));
            }
            else{
                jTextField_accountssalestotaldis.setText("0.00");
            }
            if(trc > 0){
                totalnet = new BigDecimal("0");
                for(int i = 0; i < trc; i++){
                    totalnet = totalnet.add(new BigDecimal(jTable_accountssales.getValueAt(i, 5).toString()));
                }
                jTextField_accountssalestotalnet.setText(String.format("%.2f", totalnet));
            }
            else{
                jTextField_accountssalestotalnet.setText("0.00");
            }
        }
    }//GEN-LAST:event_jButton_accountssalesgenerateActionPerformed

