    private void jButton_salesviewdeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_salesviewdeleteActionPerformed
        int sri[] = jTable_salesview.getSelectedRows();
        String sri_id[] = new String[sri.length];
        DefaultTableModel dtm = (DefaultTableModel)jTable_salesview.getModel();
        if(sri.length > 0){
            try{
                for(int i = sri.length - 1; i >= 0; i--){
                    sri_id[i] = jTable_salesview.getModel().getValueAt(sri[i], 0).toString();
                    Statement stmt = MysqlConnection.conn().createStatement();
                    ResultSet rs = stmt.executeQuery("SELECT sales_item.medicine, sales_item.quantity FROM sales_item WHERE sales_item.sales = '"+sri_id[i]+"'");
                    while(rs.next()){
                        stockMediQtyDeleteSales(rs.getString("sales_item.medicine"), Double.parseDouble(rs.getString("sales_item.quantity")));
                        stmt.executeUpdate("UPDATE medicine SET quantity = '"+String.format("%.2f", stmediqty)+"' WHERE id = '"+rs.getString("sales_item.medicine")+"'");                        
                    }
                    stmt.executeUpdate("DELETE FROM sales_item WHERE sales_item.sales = '"+sri_id[i]+"'");
                    stmt.executeUpdate("DELETE FROM sales WHERE sales.id = '"+sri_id[i]+"'");                    
                    dtm.removeRow(sri[i]);
                }
                dtm.setRowCount(0);
                loadTbl();
                JOptionPane.showMessageDialog(null, "Data deleted successfully!");
            }
            catch(Exception exc){
                exc.printStackTrace();
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Please select the row to delete data!");
        }
    }//GEN-LAST:event_jButton_salesviewdeleteActionPerformed

