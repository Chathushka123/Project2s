    private void loadTbl(){
        try{
            DefaultTableModel dtm = (DefaultTableModel)jTable_salesview.getModel();
            ResultSet rs = mc.getData("SELECT sales.id, patient.name, sales.amount, sales.discount, sales.net_amount, sales.date FROM sales, patient WHERE sales.patient = patient.id");
            while(rs.next()){
                Vector vtr = new Vector();
                vtr.add(rs.getString("sales.id"));
                vtr.add(rs.getString("sales.date"));
                vtr.add(rs.getString("patient.name"));
                vtr.add(rs.getString("sales.amount"));
                vtr.add(rs.getString("sales.discount"));
                vtr.add(rs.getString("sales.net_amount"));
                dtm.addRow(vtr);
            }
        }
        catch(Exception exc){
            exc.printStackTrace();
        }
    }
