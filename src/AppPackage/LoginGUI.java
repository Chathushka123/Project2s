    private void jButton_logincreateaccountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_logincreateaccountActionPerformed
        if(jTextField_loginname.getText().isEmpty() || jTextField_loginemail.getText().isEmpty() || jTextField_logincreateusername.getText().isEmpty() || jPasswordField_logincreatepassword.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Please fill the fields!");
        }
        else{
            try{
                boolean user = true;
                boolean mail = true;
                ResultSet rs = mc.getData("SELECT email, username FROM user");
                while(rs.next()){
                    if(rs.getString("username").equals(jTextField_logincreateusername.getText())){
                        user = false;
                    }
                    if(rs.getString("email").equals(jTextField_loginemail.getText())){
                            mail = false;
                        }
                }
                if(user == true){
                    if(mail == true){
                        mc.putData("INSERT INTO user (name, email, username, password) VALUES ('"+jTextField_loginname.getText()+"', '"+jTextField_loginemail.getText()+"', '"+jTextField_logincreateusername.getText()+"', '"+jPasswordField_logincreatepassword.getText()+"')");
                        jTextField_loginname.setText(null);
                        jTextField_loginemail.setText(null);
                        jTextField_logincreateusername.setText(null);
                        jPasswordField_logincreatepassword.setText(null);
                        JOptionPane.showMessageDialog(null, "Account Created Successfully!");
                    }
                    else{
                        JOptionPane.showMessageDialog(null, "You already have an account!");
                        jTextField_loginname.setText(null);
                        jTextField_loginemail.setText(null);
                        jTextField_logincreateusername.setText(null);
                        jPasswordField_logincreatepassword.setText(null);                        
                    }
                }
                else{
                    JOptionPane.showMessageDialog(null, "Username already exists. Please try another!");
                    jTextField_logincreateusername.setText(null);
                    jPasswordField_logincreatepassword.setText(null);
                }
            }
            catch(Exception exc){
                exc.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton_logincreateaccountActionPerformed

    private void jButton_loginloginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_loginloginActionPerformed
        if(jTextField_loginusername.getText().isEmpty() || jPasswordField_loginpasswordnew.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Please enter correct username and password!");
        }
        else{
            try{
                ResultSet rs = mc.getData("SELECT username, password FROM user WHERE username = '"+jTextField_loginusername.getText()+"' AND password = '"+jPasswordField_loginpasswordnew.getText()+"'");
                if(rs.next()){
                    HomeGUI home = new HomeGUI();
                    home.setVisible(true);
                    setVisible(false);
                }
                else{
                    JOptionPane.showMessageDialog(null, "Wrong username and password!");
                    jTextField_loginusername.setText(null);
                    jPasswordField_loginpasswordnew.setText(null);
                }
            }
            catch(Exception exc){
                exc.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton_loginloginActionPerformed

