    private void jButton_supplierviewdeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_supplierviewdeleteActionPerformed
        int sri[] = jTable_supplierview.getSelectedRows();
        String sri_id[] = new String[sri.length];
        String pursup[] = new String[sri.length];
        DefaultTableModel dtm = (DefaultTableModel)jTable_supplierview.getModel();
        if(sri.length > 0){
            try{
                for(int i = sri.length - 1; i >= 0; i--){
                    sri_id[i] = jTable_supplierview.getModel().getValueAt(sri[i], 0).toString();
                    Statement stmt = MysqlConnection.conn().createStatement();
                    
                    ResultSet rspi = stmt.executeQuery("SELECT purchase.supplier FROM purchase WHERE supplier = '"+sri_id[i]+"'");
                    if(rspi.first()){
                        pursup[i] = rspi.getString("purchase.supplier");
                    }
                    else{
                        pursup[i] = "";
                    }
                    if(pursup[i].isEmpty()){
                        stmt.executeUpdate("DELETE FROM supplier WHERE id = '"+sri_id[i]+"'");
                        dtm.removeRow(sri[i]);
                        JOptionPane.showMessageDialog(null, "Data deleted successfully!");
                    }
                    else{
                        JOptionPane.showMessageDialog(null, "Sorry, cannot delete this supplier!");
                    }
                }
                dtm.setRowCount(0);
                loadTbl();
            }
            catch(Exception exc){
                exc.printStackTrace();
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Please select the row to delete data!");
        }
    }//GEN-LAST:event_jButton_supplierviewdeleteActionPerformed

