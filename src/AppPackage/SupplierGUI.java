    private void loadTbl(){
        try{
            DefaultTableModel dtm = (DefaultTableModel)jTable_supplierview.getModel();
            ResultSet rs = mc.getData("SELECT id, name, address, phone, email FROM supplier");
            while(rs.next()){
                Vector vtr = new Vector();
                vtr.add(rs.getString("id"));
                vtr.add(rs.getString("name"));
                vtr.add(rs.getString("address"));
                vtr.add(rs.getString("phone"));
                vtr.add(rs.getString("email"));
                dtm.addRow(vtr);
            }
        }
        catch(Exception exc){
            exc.printStackTrace();
        }
    }
