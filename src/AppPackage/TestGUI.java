    private void jButton_testviewdeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_testviewdeleteActionPerformed
        int sri[] = jTable_testview.getSelectedRows();
        String sri_id[] = new String[sri.length];
        String labrestst[] = new String[sri.length];
        DefaultTableModel dtm = (DefaultTableModel)jTable_testview.getModel();
        if(sri.length > 0){
            try{
                for(int i = sri.length - 1; i >= 0; i--){
                    sri_id[i] = jTable_testview.getModel().getValueAt(sri[i], 0).toString();
                    Statement stmt = MysqlConnection.conn().createStatement();
                    ResultSet rslri = stmt.executeQuery("SELECT lab_result.test FROM lab_result WHERE test = '"+sri_id[i]+"'");
                    if(rslri.first()){
                        labrestst[i] = rslri.getString("lab_result.test");
                    }
                    else{
                        labrestst[i] = "";
                    }
                    if(labrestst[i].isEmpty()){
                        stmt.executeUpdate("DELETE FROM test WHERE id = '"+sri_id[i]+"'");
                        dtm.removeRow(sri[i]);
                        JOptionPane.showMessageDialog(null, "Data deleted successfully!");
                    }
                    else{
                        JOptionPane.showMessageDialog(null, "Sorry, cannot delete this test!");
                    }
                }
                dtm.setRowCount(0);
                loadTbl();
            }
            catch(Exception exc){
                exc.printStackTrace();
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Please select the row to delete data!");
        }
    }//GEN-LAST:event_jButton_testviewdeleteActionPerformed

